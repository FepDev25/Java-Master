package com.cultodeportivo.p2_operadores.p6_operadores_unarios;

public class OperadoresUnarios {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        System.out.println("a++ = " + (a++));
        System.out.println("a = " + a);

        System.out.println("++a = " + (++a));
        System.out.println("a = " + a);

        System.out.println("b-- = " + (b--));
        System.out.println("b = " + b);

        System.out.println("--b = " + (--b));
        System.out.println("b = " + b);
    }
}
