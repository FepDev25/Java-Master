package com.cultodeportivo.p2_operadores.p2_operadores_asignacion;

public class OperadoresAsignacion {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        a += 5; // a = a + 5
        b -= 5; // b = b - 5
        c *= 5; // c = c * 5

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
